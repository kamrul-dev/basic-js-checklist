//3.  Simple Math Operation ( +, -, *, /, %)


let num1 = 5;
let num2 = 3;

var sum = num1 + num2;
console.log(sum);

var dif = num1 - num2;
console.log(dif);

var mul = num1 * num2;
console.log(mul);

var div = num1 / num2;
console.log(div.toFixed(2));

var remainder = num1 % num2;
console.log(remainder);